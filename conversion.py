def hex2bin(hex):
    if hex == '0':
        return '0000'
    if hex == '1':
        return '0001'
    if hex == '2':
        return '0010'
    if hex == '3':
        return '0011'
    if hex == '4':
        return '0100'
    if hex == '5':
        return '0101'
    if hex == '6':
        return '0110'
    if hex == '7':
        return '0111'
    if hex == '8':
        return '1000'
    if hex == '9':
        return '1001'
    if hex == 'a' or 'A':
        return '1010'
    if hex == 'b' or 'B':
        return '1011'
    if hex == 'c' or 'C':
        return '1100'
    if hex == 'd' or 'D':
        return '1101'
    if hex == 'e' or 'E':
        return '1110'
    if hex == 'f' or 'F':
        return '1111'
